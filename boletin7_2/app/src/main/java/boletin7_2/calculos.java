
package boletin7_2;


public class calculos {
    private short num1;
    private short num2;
    public int suma(short num1, short num2){
        int suma = num1 + num2;
        return suma;
    }
    
    
    public int resta(short num1, short num2){
        int resta = num1 - num2;
        return resta;
    }
}
